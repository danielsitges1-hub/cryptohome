<section class="max-w-4xl mx-auto px-4 py-16">
  <h1 class="text-4xl font-bold mb-4">🌍 Macroeconomía</h1>
  <p class="text-gray-400 text-lg mb-12 leading-relaxed">
    Analizamos el panorama global y local. Factores como el crecimiento del PIB, la inflación,
    las políticas monetarias de la Reserva Federal de EE. UU. (FED) y los eventos geopolíticos
    influyen directamente en el desempeño del mercado. Comprender estos elementos nos ayuda a
    identificar tendencias y riesgos.
  </p>

  <div class="grid grid-cols-1 sm:grid-cols-2 gap-6">

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">📈 CPI — Índice de Precios al Consumidor</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        Mide la variación de precios de una canasta de bienes y servicios. Es el indicador
        principal de inflación. Cuando el CPI sube más de lo esperado, los mercados suelen
        reaccionar negativamente porque implica que la FED puede subir tasas.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">👷 Desempleo</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        El reporte de empleo no agrícola (Non-Farm Payrolls) se publica cada primer viernes del mes.
        Un mercado laboral fuerte puede presionar a la FED a mantener tasas altas, lo que afecta
        negativamente a activos de riesgo como crypto y acciones de crecimiento.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">🏦 PIB — Producto Interno Bruto</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        Mide el valor total de bienes y servicios producidos en un país. Un PIB en crecimiento
        indica economía sana. Dos trimestres consecutivos de PIB negativo definen técnicamente
        una recesión, señal de alerta para los mercados.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">📉 Recesión</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        Período de contracción económica sostenida. Durante una recesión, el capital huye de
        activos de riesgo hacia refugios seguros como el dólar, los bonos del tesoro y el oro.
        Entender el ciclo económico te ayuda a posicionarte antes de que ocurra.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">💼 Earnings — Resultados Corporativos</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        Cada trimestre las empresas públicas reportan sus ganancias. Los earnings de gigantes
        como Apple, Microsoft o Tesla mueven mercados enteros. Un resultado por encima de
        expectativas puede disparar el precio; uno por debajo lo hunde.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">🚀 Crecimiento Económico</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        En fases de expansión económica, el apetito por el riesgo aumenta. Los inversores
        buscan activos con mayor rendimiento: acciones de crecimiento, crypto, mercados emergentes.
        Identificar en qué fase del ciclo estamos es clave para elegir dónde estar posicionado.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6 sm:col-span-2">
      <h2 class="text-xl font-bold mb-2 text-yellow-400">🏛️ Políticas Fiscales</h2>
      <p class="text-gray-300 text-sm leading-relaxed">
        Son las decisiones del gobierno sobre gasto público e impuestos. Un gobierno que gasta
        mucho estimula la economía a corto plazo pero puede generar inflación. Las políticas
        fiscales expansivas suelen ser alcistas para los mercados en el corto plazo, pero crean
        deuda que el mercado eventualmente descuenta.
      </p>
    </article>

  </div>
</section>
