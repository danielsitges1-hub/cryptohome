  <h1 class="text-4xl font-bold mb-4">📊 Análisis Técnico</h1>
  <p class="text-gray-400 text-lg mb-12 leading-relaxed">
    Estudiamos los movimientos históricos de los precios y el volumen de negociación de las acciones.
    Utilizamos gráficos e indicadores para identificar patrones que nos pueden sugerir posibles
    movimientos futuros del precio. Este análisis nos ayuda a determinar los puntos óptimos de
    entrada y salida para nuestras inversiones.
  </p>

  <h2 class="text-2xl font-bold mb-6 text-yellow-400">✅ Puntos Clave del Análisis Técnico</h2>

  <div class="grid grid-cols-1 sm:grid-cols-2 gap-6">

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">📈 Tendencia</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        La tendencia es la dirección general del precio: alcista (sube), bajista (baja) o lateral
        (sin dirección clara). El principio más importante del análisis técnico es operar a favor
        de la tendencia. "The trend is your friend" — nunca pelees contra el mercado.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">🧱 Soporte y Resistencia</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        El soporte es un nivel de precio donde el activo tiende a rebotar hacia arriba — es el
        "piso". La resistencia es donde tiende a frenar y bajar — es el "techo". Identificar
        estos niveles nos permite planear entradas de bajo riesgo y salidas con ganancia definida.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">📦 Volumen</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        El volumen mide cuántas acciones o contratos se negociaron en un período. Confirma la
        fuerza de un movimiento: un precio que sube con alto volumen tiene más credibilidad que
        uno que sube con poco volumen. Un rompimiento sin volumen suele ser falso.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">🕯️ Velas Japonesas</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Cada vela muestra 4 datos: precio de apertura, cierre, máximo y mínimo de un período.
        Su color indica si el precio subió (verde) o bajó (rojo). Patrones como el martillo,
        la estrella fugaz o el doji nos dan pistas sobre posibles reversiones o continuaciones
        de tendencia.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6 sm:col-span-2">
      <h3 class="text-lg font-bold mb-2 text-white">⏱️ Temporalidad</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        La temporalidad es el período de tiempo que representa cada vela en la gráfica. Una vela
        en gráfica de 1 hora representa 1 hora de precio; en gráfica diaria, un día entero.
        Usamos temporalidades altas (semanal, diaria) para identificar la tendencia macro, y
        temporalidades bajas (1h, 15min) para afinar la entrada. Siempre de mayor a menor: primero
        el contexto, luego el disparo.
      </p>
    </article>

  </div>
</section>
