  <h1 class="text-4xl font-bold mb-4">🔍 Análisis Fundamental</h1>
  <p class="text-gray-400 text-lg mb-12 leading-relaxed">
    Aquí evaluamos la "salud" de una empresa. Revisamos sus estados financieros como ingresos,
    deuda, su modelo de negocio, su ventaja competitiva y la calidad de su gestión. Buscamos
    empresas sólidas con potencial de crecimiento a largo plazo.
  </p>

  <div class="grid grid-cols-1 sm:grid-cols-2 gap-6">

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">📅 10 Años en la Bolsa</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Preferimos empresas con al menos una década cotizando en bolsa. Ese historial nos permite
        analizar cómo se comportaron en crisis, recesiones y ciclos alcistas. Una empresa nueva
        no tiene track record suficiente para confiar en ella a largo plazo.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">💰 Más Ganancias que Deuda</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Una empresa sana genera más dinero del que debe. Si la deuda supera las ganancias por
        varios años, es señal de riesgo. Revisamos el ratio deuda/capital y el flujo de caja
        libre para asegurarnos de que la empresa puede operar sin depender de préstamos constantes.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">💵 Que Pague Dividendos</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Una empresa que paga dividendos de forma consistente demuestra que genera suficiente
        efectivo para repartirlo entre sus accionistas. Es una señal de solidez financiera.
        Los dividendos también te pagan mientras esperas que el precio de la acción suba.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">📊 Reporte Trimestral de Ganancias</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Cada 3 meses las empresas públicas reportan sus resultados: ingresos, ganancias netas,
        márgenes y guía futura. Comparamos los resultados actuales contra el mismo trimestre
        del año anterior y contra las expectativas del mercado. Beat o miss marcan la diferencia.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">👔 Quién Dirige la Empresa</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        El CEO y el equipo directivo importan tanto como los números. Investigamos su historial,
        si tienen "skin in the game" (acciones propias), y su visión a largo plazo. CEOs como
        Jensen Huang (NVIDIA) o Jamie Dimon (JPMorgan) generan confianza por su track record probado.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6">
      <h3 class="text-lg font-bold mb-2 text-white">🏆 +1B de Capitalización de Mercado</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        La capitalización es el valor total de la empresa en bolsa (precio × acciones). Preferimos
        empresas con más de 1,000 millones de dólares. Las Small Caps tienen más riesgo y menos
        liquidez. Las Large Caps (+10B) ofrecen mayor estabilidad para inversión a largo plazo.
      </p>
    </article>

    <article class="bg-gray-800 rounded-xl p-6 sm:col-span-2">
      <h3 class="text-lg font-bold mb-2 text-white">📰 Buenas y Malas Noticias — El Catalizador</h3>
      <p class="text-gray-300 text-sm leading-relaxed">
        Las noticias mueven el precio a corto plazo. Un acuerdo comercial, una fusión, un escándalo
        o una demanda pueden disparar o hundir una acción en horas. El análisis fundamental te da
        el criterio para distinguir si una noticia cambia el valor real de la empresa o si es solo
        ruido temporal — y actuar en consecuencia.
      </p>
    </article>

  </div>
